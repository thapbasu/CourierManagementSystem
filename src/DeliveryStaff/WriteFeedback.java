        public int GenerateID(){
    Random rand = new Random();
  
        // Generate random integers in range 0 to 99
        int rand_int1 = rand.nextInt(100);
        return rand_int1;

    
    }
    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        int rand_int1=GenerateID();
        String feedbackname = cname.getText();
        String feedbackarea = textarea.getText();
            try {
              
           
            FileWriter Writer = new FileWriter("C:\\Java\\DeliveryStaff\\Feedback.txt",true);
            if(feedbackname.isEmpty() || feedbackarea.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all fields first");
            
            }
            else{
            Writer.write(rand_int1 + "," + feedbackname + "," + lname.getText() + "," + feedbackarea);
            Writer.write(System.getProperty("line.separator"));
            Writer.close();
            JOptionPane.showMessageDialog(null, "Feedback Sent Successfully");
            cname.setText("");
            lname.setText("");
            textarea.setText("");
            }
              
              
    
             
        } catch (Exception e) {
              JOptionPane.showMessageDialog(null, " Signed up failed");
        
        }
    }//GEN-LAST:event_submitActionPerformed

